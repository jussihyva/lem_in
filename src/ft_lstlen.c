#include "lem_in.h"

size_t		ft_lstlen(t_list **lst)
{
	size_t			c;
	t_list			*elem;

	c = 0;
	elem = *lst;
	while (elem)
	{
		c++;
		elem = elem->next;
	}
	return (c);
}
